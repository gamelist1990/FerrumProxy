# End-to-end benchmark harness:
#
#   1. Start local UDP echo server on 40000 (backend).
#   2. Bench against 40000 directly           (BASELINE = kernel + loopback).
#   3. Start FerrumProxy with a UDP listener on 40001 -> 127.0.0.1:40000.
#   4. Bench against 40001                     (PROXIED = adds our overhead).
#   5. Diff the two and print an interpretation.
#
# Requires: the release binary at ..\target\release\ferrum-proxy.exe
#
# Run:  .\run_bench.ps1
param(
    [int]$Count = 500,
    [int]$SizeBytes = 512,
    [int]$EchoPort = 40000,
    [int]$ProxyPort = 40001,
    [switch]$Haproxy
)

$ErrorActionPreference = 'Stop'
$root = Split-Path -Parent $PSScriptRoot
$binary = Join-Path $root 'target\release\ferrum-proxy.exe'

if (-not (Test-Path $binary)) {
    Write-Host "[bench] release binary not found at $binary; running cargo build --release" -ForegroundColor Yellow
    Push-Location $root
    try { cargo build --release | Out-Host } finally { Pop-Location }
}

$configPath = Join-Path $PSScriptRoot 'bench-config.yml'
$haproxyLine = if ($Haproxy.IsPresent) { 'true' } else { 'false' }
$configText = @"
# Auto-generated by run_bench.ps1. Do not edit.
listeners:
  - id: 'udp-bench'
    name: 'UDP bench'
    bind: '0.0.0.0'
    udp: $ProxyPort
    haproxy: $haproxyLine
    rewriteBedrockPongPorts: false
    targets:
      - host: '127.0.0.1'
        udp: $EchoPort
ddosGuard:
  enabled: false
highLatency:
  enabled: false
sharedService:
  enabled: false
debug: false
"@
$configText | Set-Content -Path $configPath -Encoding UTF8

$logDir = Join-Path $PSScriptRoot 'logs'
if (-not (Test-Path $logDir)) { New-Item -ItemType Directory -Path $logDir | Out-Null }
$proxyOut = Join-Path $logDir 'proxy.out.log'
$proxyErr = Join-Path $logDir 'proxy.err.log'

Write-Host '' 
Write-Host '=== 1/4  starting echo server on 40000 ===' -ForegroundColor Cyan
$echoJob = Start-Job -ScriptBlock {
    param($script, $port)
    & powershell -NoProfile -File $script -Port $port
} -ArgumentList (Join-Path $PSScriptRoot 'udp_echo.ps1'), $EchoPort
Start-Sleep -Milliseconds 500

try {
    Write-Host ''
    Write-Host '=== 2/4  BASELINE bench (direct to echo) ===' -ForegroundColor Cyan
    $baselineOut = & powershell -NoProfile -File (Join-Path $PSScriptRoot 'udp_bench.ps1') `
        -RemoteHost '127.0.0.1' -Port $EchoPort -Count $Count -SizeBytes $SizeBytes
    $baselineOut | Write-Host

    Write-Host ''
    Write-Host "=== 3/4  starting FerrumProxy on $ProxyPort (haproxy=$haproxyLine) ===" -ForegroundColor Cyan
    $proxyProc = Start-Process -FilePath $binary `
        -ArgumentList @('--config', $configPath) `
        -RedirectStandardOutput $proxyOut -RedirectStandardError $proxyErr `
        -PassThru -WindowStyle Hidden
    Start-Sleep -Milliseconds 800

    if ($proxyProc.HasExited) {
        Write-Host "proxy exited immediately; logs:" -ForegroundColor Red
        if (Test-Path $proxyOut) { Get-Content $proxyOut | Write-Host }
        if (Test-Path $proxyErr) { Get-Content $proxyErr | Write-Host }
        throw 'proxy failed to start'
    }

    try {
        Write-Host ''
        Write-Host '=== 4/4  PROXIED bench (through FerrumProxy) ===' -ForegroundColor Cyan
        $proxiedOut = & powershell -NoProfile -File (Join-Path $PSScriptRoot 'udp_bench.ps1') `
            -RemoteHost '127.0.0.1' -Port $ProxyPort -Count $Count -SizeBytes $SizeBytes
        $proxiedOut | Write-Host

        Write-Host ''
        Write-Host '=== summary ===' -ForegroundColor Green
        # Rough diff by parsing MeanMs / P50Ms / P99Ms out of each block.
        function Get-Field($text, $name) {
            if ($text -match "(?m)^\s*$name\s*:\s*([0-9.]+)") { return [double]$matches[1] }
            return $null
        }
        $baselineText = ($baselineOut | Out-String)
        $proxiedText  = ($proxiedOut  | Out-String)
        foreach ($f in 'MeanMs','P50Ms','P90Ms','P99Ms','MaxMs','TotalMs') {
            $b = Get-Field $baselineText $f
            $p = Get-Field $proxiedText  $f
            if ($null -ne $b -and $null -ne $p) {
                $delta = $p - $b
                $ratio = if ($b -gt 0) { $p / $b } else { 0 }
                $line = '{0,-8}  baseline={1,10}  proxied={2,10}  delta={3,+9}ms  x{4}' -f `
                    $f,
                    ([string]::Format('{0:N3}', $b)),
                    ([string]::Format('{0:N3}', $p)),
                    ([string]::Format('{0:N3}', $delta)),
                    ([string]::Format('{0:N2}', $ratio))
                Write-Host $line
            }
        }
    } finally {
        if (-not $proxyProc.HasExited) {
            Stop-Process -Id $proxyProc.Id -Force
        }
    }
} finally {
    Stop-Job $echoJob -ErrorAction SilentlyContinue | Out-Null
    Remove-Job $echoJob -Force -ErrorAction SilentlyContinue | Out-Null
}

Write-Host ''
Write-Host 'done. proxy logs: benches_udp\logs\proxy.{out,err}.log' -ForegroundColor DarkGray
